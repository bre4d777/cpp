#include <iostream>

int main() {
  int l = 69;                  // integer
  double pi = 3.14;            // double (allows decimal)
  char c = 'a';                // single character
  bool b = true;               // boolean (true or false)
  std::string s = "lund lele"; // string
  std::cout << l << "\n" << pi << "\n" << c << "\n" << b << "\n" << s << "\n";
  return 0;
}
