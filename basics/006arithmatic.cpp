#include <iostream>

int main() {
  int a = 10;
  int b = 20; // double use kro for decimals
  std::cout << a + b << std::endl;
  std::cout << a - b << std::endl;
  std::cout << a++ << std::endl; // post increment
  std::cout << a-- << std::endl; // pre decrement
  std::cout << a * b << std::endl;
  std::cout << a * b << std::endl;
  std::cout << a / b << std::endl;
  std::cout << a % b << std::endl;
  return 0;
}
